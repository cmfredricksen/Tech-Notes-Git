<h2>Git Pages</h2>

<h3>How to Deploy Your Site Using Git Pages</h3>
<p>
  This, to me, was one of the trickiest things to figure out as a beginner to
  coding and web development. So I learned to follow the steps which I found on <a
    class="text-link"
    href="https://www.theodinproject.com/">The Odin Project</a
  > and after I had those down I was able to understand it better.
</p>
<p>
  Once you have pushed your repo to GitHub, then you can publish the page to the
  web using GitHub Pages. You must have an index.html file to do this.
</p>
<p>Follow these steps:</p>
<li>On GitHub, navigate to the repo you want to publish</li>
<li>Click on Settings</li>
<li>On the settings page, click on Pages in the left menu</li>
<li>Under Source change it to Master</li>
<li>Click Save</li>
<p>
  And that is it, your website is now online and you can view it by clicking the
  link where it says 'Your site is ready to be published link_to_site'. You can
  share this link with anyone you want so they can visit your site too.
</p>
<p>As a note: It may take a few minutes for your site to go live!</p>
<p>
  Another thing you can do is go back to your repo's page and go to the About
  settings, add the link to your website there and it will show up on your repo.
  Very nifty!
</p>
<p>
  This is just the beginning of what you can do with GitHub pages, but I am just
  giving you the basic info needed to publish your site, and it's free!
</p>
